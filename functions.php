/**
 * Enqueue scripts and styles.
 */
function garden_scripts() {
	wp_enqueue_style( 'garden-style', get_stylesheet_uri() );

	wp_enqueue_script( 'garden-navigation', get_template_directory_uri() . '/js/navigation.js', array(), '20120206', true );

	wp_enqueue_script( 'garden-skip-link-focus-fix', get_template_directory_uri() . '/js/skip-link-focus-fix.js', array(), '20130115', true );

	if ( is_singular() && comments_open() && get_option( 'thread_comments' ) ) {
		wp_enqueue_script( 'comment-reply' );
	}
	wp_register_style( 'montserrat-font', 'http://fonts.googleapis.com/css?family=Montserrat');
    
    wp_enqueue_style( 'montserrat-font' );

    wp_enqueue_style( 'dashicons' );
}
add_action( 'wp_enqueue_scripts', 'garden_scripts' );

